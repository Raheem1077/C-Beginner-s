        // Performing all Compound Assignment Operations on an Integer

#include<iostream>

using namespace std;

int main(){

    int a = 10;
    cout << "Value of a: " << a << endl;

    a += 5;
    cout << "Value of a += 5: " << a << endl;

    a -= 5;
    cout << "Value of a -= 5: " << a << endl;

    a *= 2;
    cout << "Value of a *= 2: " << a << endl;

    a /= 2;
    cout << "Value of a /= 2: " << a << endl;

    a %= 3;
    cout << "Value of a %= 3: " << a << endl;

    return 0;
}